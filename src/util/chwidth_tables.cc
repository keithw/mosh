/*
 * Mosh uses a very simple format for character width tables.  A table
 * is simply a string of characters, 0x110000 bytes or less long.
 * Each character represents a value for the chwidth() function to
 * return.
 *
 * '-'  Return -1 (invalid code point).
 * '0'  Return 0.
 * '1'  Return 1.
 * '2'  Return 2.
 *
 * In addition, Mosh also supports "overlay" tables, which can be
 * applied to an existing table.  One additional character value is
 * used for this:
 *
 * '='  Use the character from the original table.
 *
 * A short (less than 0x110000 bytes) table is treated as having '='
 * filling in the end of the table.
 *
 * So an overlay table can be used to apply a specific change to
 * whatever table Mosh is already using.  For example, Mosh's tables
 * have SOFT HYPHEN (U+00AD) as zero width.  Many terminal emulators
 * treat it as a character of width 1, though.  An overlay table with
 * the 173rd character set to '1', and all other character set to '=',
 * would accomplish this particular change.  A similar table can be
 * used to display East Asian Ambiguous characters as wide, instead of
 * narrow, or to handle private use area characters (Powerline is a
 * good example here).  Also, a complete table without any '='
 * characters will simply replace the existing table.
 * 
 */

